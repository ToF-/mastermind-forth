15 CONSTANT MAX-RESPONSES

CREATE NUM-POSSIBILITIES MAX-RESPONSES CELLS ALLOT

: INIT-NUM-POSSIBILITES
    NUM-POSSIBILITIES MAX-RESPONSES CELLS ERASE ;

CREATE CODEWORDS 6666 ALLOT

: INIT-CODEWORDS
    CODEWORDS 6666 1 FILL ;

: ELIMINATE-CODEWORD ( n -- )
    CODEWORDS + 0 SWAP C! ;

: VALID-CODEWORD? ( n -- f )
    CODEWORDS + C@ ;

VARIABLE PATTERN
VARIABLE TEST

: PEG? ( n -- f )
    1 MAXCOLOR 1+ WITHIN ;

: PATTERN? ( n -- f )
    TRUE SWAP
    N 0 DO
        10 /MOD
        SWAP PEG? ROT AND SWAP
    LOOP DROP ;

CREATE PATTERNCOLORS MAXCOLOR 1+ ALLOT
CREATE TESTCOLORS MAXCOLOR 1+ ALLOT


: TALLY-COLORS ( pattern,addr )
    DUP MAXCOLOR 1+  ERASE SWAP
    N 0 DO
        OVER >R
        10 /MOD
        SWAP R> +
        DUP C@ 1+ SWAP C!
    LOOP 2DROP ;

: HITS ( -- n )
    0 MAXCOLOR 1+ 1 DO
        PATTERNCOLORS I + C@
        TESTCOLORS I + C@ MIN
        +
    LOOP ;

: MISSES ( -- n )
    0 MAXCOLOR 1+ 1 DO
        PATTERNCOLORS I + C@
        TESTCOLORS I + C@
        - 0 MAX
        +
    LOOP ;

: BLACK-HITS ( -- n )
    0 PATTERN @ TEST @ 
    N 0 DO
        10 /MOD SWAP ROT
        10 /MOD SWAP ROT
        = IF
            ROT 1+ -ROT
        THEN
    LOOP 2DROP ;

: WHITE-HITS ( -- n )
    HITS BLACK-HITS - ;

: INIT-PATTERN ( n -- )
    DUP PATTERN !
    PATTERNCOLORS TALLY-COLORS ;

: INIT-TEST ( n -- )
    DUP TEST !
    TESTCOLORS TALLY-COLORS ;

: SCORE ( p,t -- b,w )
    INIT-TEST
    INIT-PATTERN
    BLACK-HITS
    WHITE-HITS ;
    
: .SCORE ( p,t -- )
    SCORE SWAP . . CR ;

1111 CONSTANT FIRST-CODEWORD

: NEXT-CODEWORD-REC ( n -- n' )
    10 /MOD SWAP DUP 6 < IF
        1+
    ELSE
        DROP RECURSE 1
    THEN
    SWAP 10 * + ;

: NEXT-CODEWORD ( n -- n' )
    DUP 6666 = IF DROP -1 ELSE NEXT-CODEWORD-REC THEN ;


VARIABLE MAX-POSSIBLE

: .POSSIBLE-CODEWORDS ( p b w -- )
    0 MAX-POSSIBLE !
    ROT FIRST-CODEWORD              \ b w p c
    BEGIN
        DUP -1 <> WHILE 
        DUP VALID-CODEWORD? IF
            2DUP SCORE                  \ b w p c b' w'
            2>R 2OVER 2R> D= IF
                DUP .
                1 MAX-POSSIBLE +!
                MAX-POSSIBLE @ 16 MOD 0= IF CR THEN
            THEN
        THEN
        NEXT-CODEWORD
    REPEAT 2DROP 2DROP 
    CR MAX-POSSIBLE ? ;

: NUM-POSSIBLE-CODEWORDS ( p b w -- n )
    0 MAX-POSSIBLE !
    ROT FIRST-CODEWORD
    BEGIN
        DUP -1 <> WHILE
        2DUP SCORE 2>R 2OVER 2R>
        D= IF 1 MAX-POSSIBLE +! THEN
        NEXT-CODEWORD
    REPEAT 2DROP 2DROP
    MAX-POSSIBLE @ ;

: .ALL-NUM-POSSIBLE-CODEWORDS ( p -- )
    DUP . ." : " CR
    INIT-NUM-POSSIBILITES
    MAX-RESPONSES 0 DO
        DUP
        RESPONSES I 2* +
        DUP C@ SWAP 1+ C@ 
        2DUP SWAP . . ." : "
        NUM-POSSIBLE-CODEWORDS . CR
    LOOP DROP .s ;

INIT-CODEWORDS

: REMOVE-CODEWORDS ( p b w -- )
    ROT FIRST-CODEWORD
    BEGIN
        DUP -1 <> WHILE
        DUP VALID-CODEWORD? IF
            2DUP SCORE 2>R 2OVER 2R>
            D<> IF DUP ELIMINATE-CODEWORD THEN
        THEN
        NEXT-CODEWORD
    REPEAT 2DROP 2DROP ;

: .CODEWORDS
    FIRST-CODEWORD
    BEGIN
        DUP -1 <> WHILE
        DUP VALID-CODEWORD? IF
            DUP .
        THEN
        NEXT-CODEWORD
    REPEAT DROP ;

: .CODEWORD-INDEX
    0 FIRST-CODEWORD
    BEGIN
        DUP -1 <> WHILE
        OVER . DUP .
        OVER 36 MOD 35 =  IF CR THEN
        NEXT-CODEWORD
        SWAP 1+ SWAP
    REPEAT ;
