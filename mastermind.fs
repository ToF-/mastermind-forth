: MIN-MAX-ALL-RESULT-MATCH ( -- cw )
    ALL-CODES-SET INIT-SET
    10000 MIN-MAX-RESULT !
    0 MIN-MAX-CODEWORD
    POSSIBLE-CODES START-SET
    BEGIN
        POSSIBLE-CODES NEXT-CODEWORD ?DUP WHILE
        POSSIBLE-CODE !
        POSSIBLE-CODE @ ALL-CODES-SET ALL-RESULTS-MATCH
        ALL-RESULTS-MAX 
        MIN-MAX-RESULT @ OVER > IF
            MIN-MAX-RESULT !
            POSSIBLE-CODE @ MIN-MAX-CODEWORD !
        ELSE
            DROP
        THEN
    REPEAT MIN-MAX-CODEWORD @ ;

