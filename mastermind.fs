: INDEX>CODEWORD ( i -- n )
    6 /MOD 6 /MOD 6 /MOD
    0 4 0 DO 10 * SWAP 1+ + LOOP ;

: CODEWORD>INDEX ( n -- i )
    10 /MOD 10 /MOD 10 /MOD
    0 4 0 DO 6 * SWAP 1- + LOOP ;

: CHECKINDEX
    1296 0 DO
        I DUP . INDEX>CODEWORD DUP . CODEWORD>INDEX . CR 
    LOOP ;

