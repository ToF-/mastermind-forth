6 CONSTANT MAXCOLOR
1296 CONSTANT MAXCODEWORDS
MAXCODEWORDS 8 / CONSTANT SETSIZE

: INDEX>CODEWORD ( i -- n )
    MAXCOLOR /MOD MAXCOLOR /MOD MAXCOLOR /MOD
    0 4 0 DO 10 * SWAP 1+ + LOOP ;

: CODEWORD>INDEX ( n -- i )
    10 /MOD 10 /MOD 10 /MOD
    0 4 0 DO MAXCOLOR * SWAP 1- + LOOP ;


: MATCHES ( cw,tw -- n )
    0 -ROT
    4 0 DO
        10 /MOD SWAP ROT
        10 /MOD SWAP ROT
        = IF ROT 1+ -ROT THEN
    LOOP 2DROP ;

CREATE SECRET-COLORS 7 ALLOT
CREATE TEST-COLORS 7 ALLOT

: .PEG ( p -- )
    DUP 1 = IF
        ." ⚫"
    ELSE DUP 2 = IF
        ." 🟣"
    ELSE DUP 3 = IF
        ." 🟢"
    ELSE DUP 4 = IF
        ." 🔵"
    ELSE DUP 5 = IF
        ." 🔴"
    ELSE
        ." 🟠"
    THEN THEN THEN THEN THEN DROP ;

: .PEGS ( cw -- )
    10 /MOD 10 /MOD 10 /MOD .PEG .PEG .PEG .PEG SPACE SPACE ;

: TALLY-COLORS ( cw,addr -- )
    >R R@ 7 ERASE
    10 /MOD SWAP R@ + 1 SWAP +!
    10 /MOD SWAP R@ + 1 SWAP +!
    10 /MOD SWAP R@ + 1 SWAP +!
    R> + 1 SWAP +! ;

: HITS ( cw,tw -- n )
    TEST-COLORS TALLY-COLORS
    SECRET-COLORS TALLY-COLORS
    0 MAXCOLOR 1+ 1 DO
        SECRET-COLORS I + C@
        TEST-COLORS I + C@ MIN +
    LOOP ;

: MISSES ( cw,tw -- n )
    2DUP HITS -ROT MATCHES - ;

: MATCH ( cw,tw -- bw )
    2DUP MISSES 10 * -ROT
    MATCHES + ;

15 CONSTANT MAXRESULT
CREATE RESULT-VALUES
04 C, 03 C, 02 C, 01 C, 00 C, 13 C, 12 C, 11 C, 10 C, 22 C, 21 C, 20 C, 31 C, 30 C, 40 C,

: RESULT-INDEX ( bw -- n )
    0 SWAP
    MAXRESULT 0 DO
        DUP RESULT-VALUES I + C@ = IF
            NIP I SWAP LEAVE
        THEN
    LOOP DROP ;

CREATE RESULT-OCCURENCES MAXRESULT CELLS ALLOT

: INIT-RESULT-OCCURRENCES
    RESULT-OCCURENCES MAXRESULT CELLS ERASE ;

: RESULT-OCCURENCE++ ( bw -- )
    RESULT-INDEX CELLS RESULT-OCCURENCES + 1 SWAP +! ;

: MAX-RESULT-OCCURENCE ( -- n )
    0 MAXRESULT 0 DO
        RESULT-OCCURENCES I CELLS + @ MAX
    LOOP ;

CREATE CODE-SCORES MAXCODEWORDS CELLS ALLOT

: INIT-CODE-SCORES
    CODE-SCORES MAXCODEWORDS CELLS ERASE ;

: CODEWORD-SCORE ( cw -- addr )
    CODEWORD>INDEX CELLS CODE-SCORES + ;

: MINIMUM-CODEWORD-SCORE ( -- n )
    10000 MAXCODEWORDS 0 DO
        CODE-SCORES I CELLS + @ MIN
    LOOP ;

: CODEWORD-SET ( <name> -- )
    CREATE 0 , SETSIZE ALLOT ;

: INIT-SET ( set -- )
    CELL+ SETSIZE 255 FILL ;

: START-SET ( set -- )
    -1 SWAP ! ;

: VALID-CODEWORD? ( set -- f )
    DUP CELL+ SWAP @ 8 /MOD ROT + C@ 1 ROT LSHIFT AND ;

: (NEXT-CODEWORD) ( set -- )
    BEGIN
        DUP 1 SWAP +!
        DUP @ MAXCODEWORDS <
        OVER VALID-CODEWORD? 0= AND WHILE
    REPEAT DROP ;

: NEXT-CODEWORD ( set -- n|0 )
    DUP (NEXT-CODEWORD)
    @ DUP MAXCODEWORDS < IF
        INDEX>CODEWORD
    ELSE
        DROP 0
    THEN ;

: CODEWORD-OFFSET ( cw,set -- addr )
    CELL+ SWAP CODEWORD>INDEX 8 /MOD ROT + ;

: CODEWORD-BITSET ( addr -- bs )
    C@ ROT 1 SWAP LSHIFT ;

: INSERT-CODEWORD ( cw,set -- )
    CODEWORD-OFFSET
    DUP CODEWORD-BITSET
    OR SWAP C! ;

: ELIMINATE-CODEWORD ( cw,set -- )
    CODEWORD-OFFSET
    DUP CODEWORD-BITSET
    255 XOR AND SWAP C! ;

VARIABLE POSSIBLE-CODE
VARIABLE KNUTH-CODE
VARIABLE MINIMUM-SCORE

CODEWORD-SET KNUTH-CODES
CODEWORD-SET POSSIBLE-CODES

: MINIMAX ( gset -- )
    DUP CELL+ SETSIZE ERASE
    POSSIBLE-CODES START-SET
    BEGIN
        POSSIBLE-CODES NEXT-CODEWORD ?DUP WHILE
        POSSIBLE-CODE !
        KNUTH-CODES START-SET
        BEGIN
            KNUTH-CODES NEXT-CODEWORD ?DUP WHILE
            KNUTH-CODE !
            POSSIBLE-CODE @ KNUTH-CODE @ MATCH
            RESULT-OCCURENCE++
        REPEAT
        MAX-RESULT-OCCURENCE 
        POSSIBLE-CODE @ CODEWORD-SCORE !
    REPEAT
    MINIMUM-CODEWORD-SCORE MINIMUM-SCORE !
    POSSIBLE-CODES START-SET
    BEGIN                                      \ gset
        POSSIBLE-CODES NEXT-CODEWORD ?DUP WHILE \ gset,cw
        DUP CODEWORD-SCORE @ MINIMUM-SCORE @ = IF
            OVER INSERT-CODEWORD
        ELSE
            DROP
        THEN
    REPEAT DROP ;

VARIABLE MATCHING-SET

: .MATCHING-CODEWORDS ( cw,bw,set -- )
    .s
    ROT DUP >R -ROT R> .PEGS CR
    DUP MATCHING-SET !
    DUP INIT-SET START-SET
    BEGIN
        MATCHING-SET @ NEXT-CODEWORD ?DUP WHILE   \ cw,bw,p
        POSSIBLE-CODE !
        2DUP SWAP POSSIBLE-CODE @ MATCH = IF
            POSSIBLE-CODE @ .PEGS
        THEN
    REPEAT 2DROP ;
    


