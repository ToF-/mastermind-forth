: RESULT-SET ( n -- set )
    ASSERT( DUP 0 15 WITHIN )
    MAX-CODEWORDS 8 / CELL+ * RESULT-SETS + ;

: ALL-RESULTS-MATCH ( cw,srce -- )
    15 0 DO
        2DUP I RESULT-VALUE
        SWAP I RESULT-SET    \ cw,srce,cw,r,srce,dest
        RESULT-MATCH-SET
    LOOP 2DROP ;

: ALL-RESULTS-MAX ( -- n )
    0 15 0 DO I RESULT-SET SET-SIZE MAX LOOP ;

: .RESULT ( r -- )
    DUP 0= IF
        ." __" DROP
    ELSE
        10 /MOD 
        0 ?DO ." ●" LOOP
        0 ?DO ." ○" LOOP
    THEN ;

: .ALL-RESULTS
    15 0 DO
        I 2 .R [CHAR] ) EMIT SPACE I RESULT-VALUE .RESULT SPACE
        I RESULT-SET DUP SET-SIZE . .SET CR
    LOOP
    0 15 0 DO I RESULT-SET SET-SIZE MAX LOOP
    ." max = " . cr ;

