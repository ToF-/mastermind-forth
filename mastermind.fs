VARIABLE GUESS

VARIABLE COUNTER

: GUESS-CODEWORD
    CR
    POSSIBLE-CODES INIT-SET 
    COUNTER OFF
    1122 GUESS !
    BEGIN
        1 COUNTER +!  COUNTER ?
        GUESS @ . [CHAR] : EMIT SPACE
        GUESS @ POSSIBLE-CODES ALL-RESULTS-MATCH
        15 0 DO I RESULT-VALUE .RESULT SPACE I RESULT-SET SET-SIZE . [CHAR] , EMIT SPACE LOOP CR
        POSSIBLE-CODES DUP SET-SIZE 1 > WHILE
        SECRET GUESS @ MATCH DUP .RESULT CR
        RESULT-INDEX RESULT-SET POSSIBLE-CODES COPY-SET
        DUP START-SET NEXT-CODEWORD GUESS !
    REPEAT ;

CODE-SET ALL-CODES-SET

VARIABLE MIN-MAX-RESULT
VARIABLE MIN-MAX-CODEWORD
